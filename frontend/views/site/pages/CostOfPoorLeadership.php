        <p>As I left the man’s office, I couldn’t help but think his company 
            was succeeding despite him. It could have been the way he talked in 
            terms of &ldquo;I&rdquo; instead of &ldquo;we&rdquo;, how he talked 
            about making all the decisions instead of empowering his people, 
            or how most of his people worked there not because they wanted to, 
            but solely because of the paycheck.</p> 
        <p>So much has been written about great leadership and the success that 
            happens when men and women emerge to lead people in a way that 
            creates the best version of themselves. But a different question 
            has been gnawing at me after I left his office:</p> 
        <blockquote>
            What does it truly cost us to have poor leadership? 
        </blockquote> 
        <p>After a lot of research and reflection, it begins with the knowledge 
            that cost isn’t just monetary. When you look at the definition of 
            cost, it talks about "outlay or expenditure." Sure there are 
            countless examples of where the outlay or expenditure of poor 
            leadership was millions if not billions of dollars, but here are 
            some of the real costs of poor leadership:</p> 

        <h3><strong>Poor Daily Experiences</strong></h3> 
        <p>I was speaking with a lady a couple months ago and she was telling 
            me about her experience at her former company and it's something I 
            will never forget. “Some days I would pull into the office and 
            see the cars of the people I worked with and then I would wait 20 
            minutes in the car before going inside, because I knew how long the 
            day was going to be working with the team.” At the end of the 
            day, we spend more time with the people at work than we do with our 
            own families and it’s almost tragic to know one of the main costs of 
            poor leadership is unhappy, unfulfilled and uninspired people at 
            work. </p> 

        <h3><strong>Talented People Leaving</strong></h3> 
        <p>When I asked this to Pat Lencioni on the 
            <a href="http://apple.co/2uVgX3S" target="_blank" rel="nofollow noopener">Follow My Lead</a> 
            podcast he said; “When you have bad leadership, the best employees 
            on the team will begin looking for a different environment.” He is 
            spot on because the best performers don’t settle. It’s average and 
            below average performers who settle for poor leadership. Ask any 
            sports coach or VP Sales the impact of losing their top 3 or 4 
            best team members and you will see their pain just by looking into 
            their eyes.</p> 

        <h3><strong>A Toxic Culture</strong></h3> 
        <p>Culture is everything to a team and while everyone on the team plays 
            a part in the ongoing development of the culture, it’s the leader's 
            responsibility to create and own it. Former Carolina Panthers 
            All-Pro Mike Rucker said this 
            <a href="http://bit.ly/2eSL30D" target="_blank" rel="nofollow noopener">week on the show</a>, 
            “It’s the culture a leader sets that dictates the path.” Toxic 
            cultures create bad habits and bad habits eventually lead to bad 
            results. Conversely great cultures create good habits and good 
            habits eventually lead to positive results. </p> 

        <h3><strong>Long Term Success</strong></h3> 
        <p>I have written about this in detail when describing the Ruler profile 
            from the Welder Leader program. &nbsp;Most leaders who come in with 
            a hard set of rules and standards typically see some short term gain 
            in performance. Unfortunately, it’s difficult to sustain success 
            over multiple years if not decades because people get worn down and 
            beat up. Think of it like marriage. If one of the partners isn’t in 
            it for the right reasons, over time the marriage will eventually 
            break down. Same goes with poor leadership, eventually, people 
            catch on and when they do, it all goes downhill.</p> 

        <h3><strong>Poor Health or Even Death</strong></h3> 
        <p>When I asked this question to David Marquet I was shocked at his 
            response. “There is a tragic cost in terms of health.” He spoke in 
            detail, about a study completed after the Great Depression looking 
            at people in the US who had died based on the demand of their job 
            and control they had in that job. Turns out those who had high 
            demanding jobs but no control had a 25% higher likelihood of dying 
            earlier than those who had high demanding jobs but control over 
            decision making. While I question the study a little bit, when you 
            look throughout history, poor health and even death has followed 
            around some of the worst leaders who ever walked the earth. Not 
            only physical death but emotional death as well. From chronic 
            stress to obesity, to alcoholism, drug use, etc.</p>

        <p>So the next time you start thinking about whether you should care 
            about being a great leader or not, think through what the real cost 
            of poor leadership is and you should have your answer.</p>

