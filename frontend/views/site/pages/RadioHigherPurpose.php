        <p>In our busy lives it’s hard enough to keep up with employees, 
            advertisers, promotions, dealing with budgets, and all the things 
            that come with running a radio station. Yet, I found in my life as 
            a broadcaster the most gratifying moments in all my years of station 
            ownership were those moments when we leveraged our brand and our 
            listeners to help others. Tony Robbins says “living is giving,” and 
            my best memories in radio have to do with those giving moments.</p>
        <p>I want to tell you a brief story of radio giving, which may lead you 
            to some ideas for your station.</p>
        <p>Did you know that there is an organization that builds specially 
            adapted homes for our nation’s severely wounded soldiers? Hardly 
            anyone knew, until WDAY 970 AM in Fargo, ND, started talking about 
            it with a new show.</p>
        <p><em>Heroes of the Heartland</em>, hosted by Master Sgt. Eric Marts, 
            is a proud supporter of the non-profit “Homes for our Troops.” This 
            charity constructs these uniquely built homes, and provides these 
            veterans with mortgage-free housing. Marts was wounded by a IED in 
            Iraq, suffering a brain injury, total blindness, and damage to his 
            arms, neck, and back. He started his WDAY radio show five years ago, 
            following his discharge from the U.S. Army.</p>
        <p>On his show, Marts discusses veterans’ issues, the military and 
            military families, but the show also fuels awareness and donations, 
            which helps more veterans.</p>
        <p>While local TV stations could only devote a short news segment to the 
            subject, or a PSA about “Homes for our Troops,” it is radio that has 
            devoted a weekly show to raise issues that may greatly impact radio 
            listeners in Fargo and Moorhead, Minnsota, where Marts resides.</p>
        <p>Marts’ show airs Saturday mornings on Forum Communications’ WDAY, 
            which has resulted in raising awareness of veterans’ issues, 
            creating a popular program that gets the community’s attention and 
            simultaneously helps fuel donations to this worthy charity.</p>
        <p>This, my friends, is radio at its best. &nbsp;The station’s airtime 
            could easily have been brokered to a financial advisor, or a health 
            and diet supplement vendor. Instead, WDAY took a step back and gave 
            the community something unique, and valuable, which serves a higher 
            purpose. Though financial success is critical, there is no way you 
            can buy this kind of community goodwill.</p>
        <p>What if your station could do the same in your market? Could you 
            create a show and use the show as leverage to build homes for vets 
            in your town? Or, could your station take on some other major issue 
            in your community as your annual project, with a goal of changing 
            lives? You would not only be driving attention to your station, but 
            you would be taking on a major issue that helps people in your 
            community.</p>
        <p>What if we could get a single station in every market in America to 
            take this particular issue on? <em>Radio</em> could play a major 
            role in helping veterans in every community. Therefore, I’d like to 
            urge you to consider taking this effort on in your community and 
            adopt it. If this isn’t your cup of tea, what could you do that you 
            would get excited about?</p>
        <p>I’d like to salute Master Sgt. Eric Marts and WDAY’s owner and 
            employees for their commitment to the community. WDAY took a step 
            back and gave the community something unique and valuable.</p>
        <p>This is what makes radio unique and valuable. These are the 
            meaningful moments you’ll look back on knowing that you used your 
            station for its highest and best purpose.</p>
        <p>Marts recently reached out to <em>Radio Ink</em> to help spread the 
            word about “Homes for our Troops,” so your station may consider a 
            PSA devoted to the charity. Or, a Sunday morning public service 
            broadcast could include an interview with a charity representative. 
