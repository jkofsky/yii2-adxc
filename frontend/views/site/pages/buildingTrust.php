<p>Trust is a core ingredient to build successful relationships. Both personal
    and professional ones. It is a major leadership characteristic. However,
    you can´t take it for granted. You need to work hard to earn trust and to
    keep it.</p>
<p>Isaac Watts once said that "Learning to trust is one of life's most
    difficult tasks.“ If you´re not seen as a trustworthy person you can
    neither form engaged relationships nor high performing teams. And without
    them you can´t become a successful leader and manager. If you were not
    careful, you can lose trust within days or even hours.</p>
<p>In today´s article I´d like to share with you my thoughts and what I
    consider being the most important principles to build, regain, and
    sustain trust:</p>

<ol>
    <li>
        <strong>Walk Your Talk.</strong>
        <p>Mean what you say and keep your word. Deliver your committed tasks
            and duties on time and in full. Be consistent and reliable. Arrive
            promptly to meetings. If you might risk missing an agreed timeline,
            proactively communicate and explain it, apologize and come forward
            with a new proposal. Try not missing it a second time to protect your
            reputation. Lead by example and permanently demonstrate that you
            deliver on your promises and that others can count on you.</p>
    </li>
    <li>
        <strong>Communicate Frequently And Openly.</strong>
        <p>Direct, quick, accurate, and honest communication builds trust. Share
            regularly with others. This underlines that you have no hidden agenda.
            Address possible trust issues within a team in an open and prompt
            manner. In this context also crucial to develop and possess good
            active listening skills.</p>
    </li>
    <li>
        <strong>Tell The Truth And Take A Stand.</strong>
        <p>Be honest and don´t lie. This is not as easy as it might sound. Of
            course, we wouldn´t admit it, would we? Anyway, it´s key not leaving
            out relevant facts, figures, and opinions when discussing and arguing.
            Even, and especially, when it´s awkward and painful. People will
            appreciate it. Also be willing to say no. You can´t be everything to
            everyone. Taking a stand based on sound arguments - well and politely
            articulated - will earn you respect and trust.</p>
    </li>
    <li>
        <strong>Be Transparent And Unite.</strong>
        <p>Share your objectives, strategy, agenda, and values. People want to
            know what you think and believe and to understand how they might fit
            into the picture. This gives them security and confidence in you and
            your intentions. Create a common identity and establish a sense of
            companionship.</p>
    </li>
    <li>
        <strong>Show People That You Care About Them.</strong>
        <p>Be out for others and not primarily for yourself. Appreciate all
            people you´re dealing with. Show sensitivity to their interests,
            wishes, and needs. Value them and thank them. Express sincere
            gratitude rather two times too often than missing it just once. Do
            it from the heart.</p>
    </li>
    <li>
        <strong>Empower Others.</strong>
        <p>Show people that you trust them. Grant flexibility, stimulate
            initiative-taking, and ask for regular feedback. Have faith in others
            skills and capabilities. Be willing to let go and to share power. "The
            best way to find out if you can trust somebody is to trust them"
            (E. Hemingway).</p>
    </li>
    <li>
        <strong>Focus On The Positives.</strong>
        <p>Don´t punish mistakes. As they can happen think and speak about them
            in a results-oriented and forward-looking way. Jointly look with
            others for solutions and implement actions to avoid that they´ll
            happen again.</p>
    </li>
    <li>
        <strong>Coach And Train Others.</strong>
        <p>Guide people around you and assist them in finding their way and the
            right solutions for themselves. Don´t tell them what to do. Instead
            ask them for their opinions. Support them in becoming great and
            outstanding.</p>
    </li>
    <li>
        <strong>Follow High Ethical Standards.</strong>
        <p>Do the right things. Even, and especially, when it might be hard.
            People will like, respect, and trust your integrity.</p>
    </li>
    <li>
        <strong>Admit Mistakes And Weaknesses.</strong>
        <p>Fortunately, you´re only human. So, when you don’t do the right thing,
            admit it. Be transparent, authentic and willing to talk about your
            mistakes and faults in a constructive manner. When you are vulnerable
            and have nothing to hide you radiate trust. That´s what people love.</p>
    </li>
    <li>
        <strong>Establish Long Term Relationships.</strong>
        <p>Trust is usually not the result of short term actions and profits. It´s
            stemming from deeper values, ethics, and fundamental principles. Take
            your time and don´t rush. Be willing to let trust evolve and flourish.
            The best trust fertilizer is to "give without any strings attached.“
            Don´t expect always something in return.</p>
    </li>
</ol>