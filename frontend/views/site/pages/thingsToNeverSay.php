<p>You want to be successful. Everyone does. But your actual words might be
    undermining your chances of success. The things you say in the office, no
    matter how innocuous they seem to you, might be knocking you down the
    career ladder and putting the top position you dream about out of reach.</p>

<p>Your career is too important to be tanked by a few negative phrases. Here
    are the seven things you should strike from your workplace vocabulary if
    you want to achieve the success you richly deserve:</p>
<ol>
    <li>
        <strong>“That’s not in my job description.”</strong>
        <p>When you accepted your current position, you had a good idea of what the
            responsibilities and workload of the role would entail. Throughout the
            months or years since you settled into your job, however, your role has
            expanded and changed shape. Some of these changes have probably been good,
            while others have made you wish for simpler times. When a boss or manager
            piles another responsibility on your already sore shoulders, it might be
            tempting to pull out this classic gem of work avoidance.</p>

        <p>The better option, however, is to schedule a time to talk to your boss about
            your role. A specific conversation about your place in the organization is
            a good time to bring up the particulars of your job description, not when
            you’re asked to get something accomplished. No matter how stressed you are
            or how valid the complaint, dropping this phrase only makes you look lazy
            and unmotivated.</p>
    </li>
    <li>
        <strong>“It can’t be done.”</strong>

        <p>Throwing in the towel makes you look like a quitter -- and quitters don’t
            get promoted. Instead of giving up on a project entirely, frame your
            response in terms of alternative ways to get the work accomplished. Very
            little is truly impossible, and most managers and executives want
            forward-thinking problem solvers to climb the corporate ladder. If you
            offer solutions instead of giving up, you’ll be seen as a valuable member
            of the team.</p>
    </li>
    <li>
        <strong>“It’s not my fault.”</strong>

        <p>No one wants to work with a blame shifter. After all, it’s just a matter of
            time before this person eventually shifts the blame onto you. Take
            ownership of your mistakes instead of pointing out where others have
            fallen short. Admitting to a mistake shows character and the ability to
            learn and grow from problems. Pointing the finger at someone else strongly
            implies you’ll never truly learn from your errors.</p>
    </li>
    <li>
        <strong>“This will just take a minute.”</strong>

        <p>Unless something will literally take only 60 seconds, don’t overpromise and
            underdeliver. Saying something will only take “a minute” also has the side
            effect of undermining your efforts. Most likely the reason the particular
            task won’t take long is due to the benefit of your professional experience
            and acumen. By saying it will “just” take a minute, you're shortchanging
            what you bring to the table.</p>
    </li>
    <li>
        <strong>“I don’t need any help.”</strong>

        <p>The rugged lone wolf type might be the hero of most action movies, but
            they’re unlikely to become the hero at your company. You might think you
            can go it alone on a project or in your career, but teamwork is essential.
            Being able to work with others is the hallmark of a good leader; you’re
            unlikely to climb your career ladder always flying solo.</p>
    </li>
    <li>
        <strong>“It’s not fair.”</strong>

        <p>Life isn’t fair, and often your career won’t be as well. Instead of
            complaining, you should look for specific and actionable workarounds to
            the problems you encounter. Is it unfair a coworker got to run point on
            the project you wanted? Maybe, but instead of complaining, work harder
            and go the extra mile. Finding a solution will always be preferable in
            your professional life to whining about a problem.</p>
    </li>
    <li>
        <strong>“This is the way it’s always been done.”</strong>

        <p>Doing things the way they’ve always been done is no way to run a business.
            Just ask some of the companies which toed the line, accepted the status
            quo, and went under. Adapting to an ever-changing marketplace is really
            the only way to survive in an economy constantly being disrupted by the
            next big thing.</p>

        <p>You don’t have to be a slave to the trends, but you also can’t stick your
            head in the sand and hope things go back to normal. Instead, come up with
            creative solutions to new problems and innovate, and you’ll soon be in the
            driver’s seat taking your organization into the future.</p>
    </li>
</ol>

<p>Everyone wants to be successful, so make sure your words aren’t holding you
    back. These seven phrases are career kryptonite -- by avoiding them, you
    can fly into your future and become a successful superstar.</p>
