<h1><?php echo Html::encode('Twelve Resolutions For Radio'); ?></h1>
<address>published by Eric Rhoads on: Radio Ink (2011)</address>

<ol>
    <li>
        <strong>Change Our Language.</strong>
        <p>Today the buzz for measurement is "monthly uniques." If it's good
            enough for Google, it's good enough for radio. Drop the term "cume."
            Start using monthly uniques in every pitch and presentation.
        </p>
    </li>
    <li>
        <strong>Kill Average Quarter Hours.</strong>
        <p>Why do we insist on complicating things? Don't sell by AQH. As
            Bob Pittman said, it may be a tool for exact placement, but it's
            not a tool for selling. In Internet terms, this is "simultaneous
            usage." But the number isn't great, so Internet companies don't
            talk about it. Why should you?
        </p>
    </li>
    <li>
        <strong>Promote Our Growth.</strong>
        <p>With all the negative press about "old media," I think we've
            started buying it. The fact is, PPM shows that more people listen
            to radio than we ever knew. Our reach in 1970 was 92.7 percent, and
            in 2010, it's 93 percent. We're going up. When was the last time
            you updated your clients?</p>
    </li>
    <li>
        <strong>Change Perceptions.</strong>
        <p>The perception of radio, perpetuated in the media, is much worse
            than the reality. Each of us needs to do our part to show the real
            truth. Get the facts, and set the record straight. Fight to tell
            radio's powerful story.</p>
    </li>
    <li>
        <strong>Look At Digital As An Opportunity. Not a threat.</strong>
        <p>Yes, digital is hot, but so is radio. Our numbers hold up against
            theirs. Digital + radio is better than digital alone.</p>
    </li>
    <li>
        <strong>Cannibalize Dying Media.</strong>
        <p>Newspapers and Yellow Pages ARE dying. Newspapers still have 16
            percent of all ad dollars, and Yellow Pages have 7 percent. That's
            a total of $31.3 billion that has to go somewhere. Why not to radio?</p>
    </li>
    <li>
        <strong>Keep Your Rates Up.</strong>
        <p>Do you want to be known as the local prostitute? Low prices and
            cutting rates send bad signals. Fight a little harder for every
            dollar. Don't give in so easily.</p>
    </li>
    <li>
        <strong>Spend More Time With Clients.</strong>
        <p>Seems simple, but management people and owners sometimes get stuck
            behind the desk. More time with clients will not only help you sell
            more, it will help you understand more. That will impact your
            business.</p>
    </li>
    <li>
        <strong>Focus On Campaigns, Not Spots.</strong>
        <p>Campaigns work. Spots don't. Clients (and your sellers) need to
            understand the difference and be shown why they will see better
            results with more time on the air, over longer periods of time.</p>
    </li>
    <li>
        <strong>Focus On Moving Product.</strong>
        <p>Rather than focusing on hitting budgets and numbers, make sure
            you're there to SERVE your advertisers. Change the focus of every
            discussion so it's about moving product and solving their problems.
            If you move product, people don't care about audience sizes or
            listening levels.</p>
    </li>
    <li>
        <strong>Overdeliver.</strong>
        <p>Got extra inventory? Fill it with more spots from your paying
            clients. You don't even need to tell them. The goal is to pack
            their stores and move more product with every tool at your
            disposal&#8230; your airtime, your website, your newsletters. Blow
            them away with what your station is willing to do to bring them
            business, and you'll have friends for life.</p>
        </dd>
    <li>
        <strong>Only Run Ads That Will Work.</strong>
        <p>Crummy ads written by our own stations, our advertisers, and local
            agencies simply don't work. Develop a test internally to determine
            whether an ad is likely to work, and if you believe it won't, fight
            to change it. The more time spent on ad effectiveness, the fewer
            clients lost because "Radio doesn't work." Remember, they are
            renting our airtime to reach our audiences, but if their ad is bad,
            we will get blamed. Help advertisers see the light by pointing out
            ads that won't work and suggesting alternatives. If they insist,
            you'll be on record as saying the ad won't work. But you'll
            probably change their mind, and change their results.</p>
    </li>
</ol>
